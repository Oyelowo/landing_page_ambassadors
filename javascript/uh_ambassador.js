function openSlideNav() {
    document.getElementById("side-menu").style.width = "10rem";
    document.getElementById("side-menu").style.height = "20rem";
    document.getElementById("main-test").style.marginLeft = "12rem";
}

// for closing the slide navigation bar
function closeSlideNav() {
    document.getElementById("side-menu").style.width = "0";
    document.getElementById("main-test").style.marginLeft = "0";
}
